package controller.editor.listeners;

import gui.editor.dialogs.AddToPackageDialog;

import java.awt.event.ActionEvent;
import java.awt.event.ActionListener;

import javax.swing.JFrame;

public class AddToPackageListener implements ActionListener {

	private JFrame parent;

	public AddToPackageListener(JFrame parent) {
		super();
		this.parent = parent;
	}

	@Override
	public void actionPerformed(ActionEvent e) {
		AddToPackageDialog dialog = new AddToPackageDialog(parent);
		dialog.setVisible(true);
	}

}
